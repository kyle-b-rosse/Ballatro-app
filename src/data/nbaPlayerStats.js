// Helper function to generate random stats based on player's base stats
export const generatePlayerStats = (player) => {
  return {
    points: Math.ceil(player.baseStats.points),
    rebounds: Math.ceil(player.baseStats.rebounds),
    assists: Math.ceil(player.baseStats.assists),
    stocks: Math.ceil(player.baseStats.stocks),  // Keep stocks as a single stat
    steals: 0,  // Keep these at 0 since we're not using them
    blocks: 0   // Keep these at 0 since we're not using them
  };
};

// Function to generate initial player pool
export const generateInitialPlayers = () => {
  return NBA_PLAYERS.map(player => ({
    ...player,
    points: 0,
    rebounds: 0,
    assists: 0,
    steals: 0,
    blocks: 0
  }));
};

// Add this helper function
export const playerHasGame = (player, games) => {
  if (!games || !games.length) return false;
  return games.some(game => 
    game.homeTeam === player.team || game.awayTeam === player.team
  );
}; 